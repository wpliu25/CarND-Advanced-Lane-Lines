def camera_setup(calibration_path='camera_cal/calibration*.jpg', nx=9, ny=6):
    '''
    camera_setup sets up calibration images and returns camera calibration results
    :param calibration_path:
    :param nx:
    :param ny:
    :return:
    '''
    # Make a list of calibration images
    cal_images = glob.glob(calibration_path)
    cam_mtx, cam_dist = camera_calibrate(cal_images, nx, ny)
    return cam_mtx, cam_dist

